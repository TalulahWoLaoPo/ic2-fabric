package ic2_120.content.block

/**
 * 能量机器接口
 *
 * 表示具有能量等级的机器方块实体。
 * 用于区分不同层级的能量设备，确保充电规则正确应用。
 */
interface ITieredMachine {
    /**
     * 机器的能量等级（1-4）
     *
     * 根据 ITiered 接口的规则，机器只能给等级 <= 自己的设备充电。
     * 例如：
     * - 等级 1 发电机：只能给等级 1 电池充电
     * - 等级 2 发电机：可以给等级 1 和等级 2 电池充电
     */
    val tier: Int
}
